    <h2>ABOUT</h2>
        <div class="leftCol">
            <h3>An Interview with our Table Maker - Will from WRW and Co.</h3>
            <h5>The tables you are making are stunning. Do you mainly use recycled materials?</h5>
            <p>Yes i use a lot of reclaimed timber - for one it’s a fantastic sustainable option and also
            with reclaimed timber they carry a history of a former life, I love timber that is full of
            character and 'historic scars'. These are all exposed carefully in the process of repurposing
            them into a new piece - dining tables, hallway tables, coffee tables, cafe tables.</p>
            <h5>When did you start making your own furniture? And what inspires you?</h5>
            <p>My inspiration is fueled by a deep appreciation of craftsmen before me whose skilled
            hands and sometimes simple tools created buildings and furniture which have survived
            many decades not only environmentally but also aesthetically where pragmatism and
            style come together in a perfect balance. It's the enjoyment in creating something unique
            with its own story and character, being able to see the historical scars in the timber, and
            the excitement on the clients face when I've delivered a table.</p>
            <p>I have always had a fascination with furniture and built my first table from reclaimed kauri
            4 years ago for the Bach in Raglan. My first effort resulted in a flurry of orders. With the
            will and the skill but lack of a formal workshop I managed to complete the orders in the
            wool shed with equipment rustled up from toolboxes around the sheds. A few sold quickly
            in one of Ponsonbys top design stores. I then returned to my job in London as a project
            manager for interior fit outs of restaurants, shops and offices.</p>
            <h5>Raglan for Will Worsp is...</h5>
            <p>Raglan for me feels like home.. From a farm in Te Akau Raglan has always been a place of
            interest and while i've been away for a while it's nice to have seen it grow in its popularity
            over the years but still keep its strong nit community and surf town values, it's NZs
            coolest little town!! Best coffee, best waves, best town! Raglan has got a bit of everything!
            It’s obvious to see by the hordes of cars heading up or down for the weekends.</p>
